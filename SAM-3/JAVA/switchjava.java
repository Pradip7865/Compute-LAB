public class switchjava {
    public static void main(String args[]) {
        int a = 3;

        switch(a){
            case 0:
            System.out.println("1st no 0 ");
            break;

            case 1:
            System.out.println("2nd number is 1");
            break;
            default:
            System.out.println(a);
        }
    }
    
}
